package io.mykit.data.plugins.config;

/**
 * @author binghe
 * @version 1.0.0
 * @description 插件
 */
public class Plugin {

    // 插件名称
    private String name;

    // 插件实现
    private String className;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClassName() {
        return className;
    }

    public Plugin setClassName(String className) {
        this.className = className;
        return this;
    }
}
